# 切片的应用：输入字符串，本函数去掉其首位的空格
# S[:1]第零个数字为空格时使用while循环将其去除，从第一个数往后取值。
# s[-1:]表示倒数第1个数字
# 0可以省略。
def trim(s):
        if s == ' ':
                s = s
        while s[:1] == ' ':
                s = s[1:]
        while s[-1:] == ' ':
                s = s[:-1]
        return s
